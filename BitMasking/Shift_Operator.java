import java.util.*;

public class Shift_Operator {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int num = sc.nextInt();
        // for num=7;
        // System.out.println(num >> 1);
        // for num=-11;
        System.out.println(num >> 1);

        // left shit
        System.out.println(num << 1);
    }
}
